    private boolean isNumeric(String parametro) {
        try {
            Integer.parseInt(parametro);
            return true;
        } catch (NumberFormatException nfe) {
            return false;

        }
    }

    private boolean validarDatosDeEntrada() {

        if (!isNumeric(this.txtHabitacion.getText())) {
            this.lblmgsRoomError.setForeground(Color.red);
            this.lblmgsRoomError.setText("Ups!!! el numero de habitacion debe ser un entero");
            return false;
        } else if (isNumeric(this.txtName.getText())) {
            this.lblmgsNameError.setForeground(Color.red);
            this.lblmgsNameError.setText("Ups!!! el Nombre debe ser en letras");
            return false;
        } else if (isNumeric(this.txtLastname.getText())) {
            this.lblmgsLastnameError.setForeground(Color.red);
            this.lblmgsLastnameError.setText("Ups!!! el Apellido debe ser en letras");
            return false;
        } else {
            return true;
        }
    }
    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed

        if (this.txtName.getText().length() != 0 && this.txtLastname.getText().length() != 0 && this.txtHabitacion.getText().length() != 0 && this.jDataBirthday.getDate() != null) {

            if (validarDatosDeEntrada()) {

                int habitacion = Integer.parseInt(this.txtHabitacion.getText());
                if (habitacion <= 100 || habitacion > 999) {
                    this.lblmgsRoomError.setForeground(Color.red);
                    this.lblmgsRoomError.setText("Ups!!! el numero de habitacion debe estar entre 100 y 999");
                } else {

                    Date fecha = jDataBirthday.getDate();
                    DateFormat f = new SimpleDateFormat("dd/MM/yyyy");
                    String fecha2 = f.format(fecha);

                    PatientDTO patient = new PatientDTO(this.txtName.getText(), this.txtLastname.getText(), Integer.parseInt(this.txtHabitacion.getText()), fecha2);
                    patient.setPatientClbk(RoomGUI.patient);
                    boolean reply=RoomGUI.ref.modifyPatient(Integer.parseInt(this.txtHabitacion.getText()),patient);
               
                    if (reply) {
                        clearLabels();
                        this.lblReply.setForeground(Color.green);
                        this.lblReply.setText("Paciente actualizado");
                    }
                    else{
                        this.lblReply.setForeground(Color.red);
                        this.lblReply.setText("Fallido en la actualizaciÃ³n: no se contacto el servidor");
                    }
                }
            }

        } else {
            if (this.txtName.getText().length() == 0) {
                this.lbl1.setForeground(Color.red);
                this.lbl1.setText("*");
            }
            if (this.txtLastname.getText().length() == 0) {
                this.lbl2.setForeground(Color.red);
                this.lbl2.setText("*");
            }
            if (this.txtHabitacion.getText().length() == 0) {
                this.lbl3.setForeground(Color.red);
                this.lbl3.setText("*");
            }
            if (this.jDataBirthday.getDate() == null) {
                this.lbl4.setForeground(Color.red);
                this.lbl4.setText("*");
            }

        }
    }//GEN-LAST:event_btnUpdateActionPerformed

